template_path = os.path.join(os.environ["SCRIPT_DIR"], "spark-pipeline-template.yml")
output_path   = os.path.join(os.environ["SCRIPT_DIR"], "spark-pipeline.yml")

with open(template_path) as fh:
    lines = fh.readlines()

def substitute(text):
    def replacer(m):
        key = m.group(1)
        return os.environ.get(key, m.group(0))
    return re.sub(r"\$\{([A-Za-z_][A-Za-z0-9_]*)\}", replacer, text)

output_lines = []
for line in lines:
    subst = substitute(line)
    # Drop config lines whose value resolved to empty (becomes YAML null)
    if re.match(r"^\s+spark\.\S+:\s*$", subst):
        continue
    output_lines.append(subst)

with open(output_path, "w") as fh:
    fh.writelines(output_lines)

print("[INFO] Generated spark-pipeline.yml from template.")
PY

