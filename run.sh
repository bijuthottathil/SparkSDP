# ── 7. Logging setup ──────────────────────────────────────────────────────────
mkdir -p "${SCRIPT_DIR}/logs"
LOG_FILE="${SCRIPT_DIR}/logs/pipeline_$(date +%Y-%m-%d_%H-%M-%S).log"
{
    echo "════════════════════════════════════════════════════════════════"
    echo " LDP Tax Pipeline — started at $(date -Iseconds)"
    echo " LOCAL_MODE  : ${LOCAL_MODE}"
    echo " WAREHOUSE   : ${LDP_WAREHOUSE_PATH}"
    echo " LOG FILE    : ${LOG_FILE}"
    echo "════════════════════════════════════════════════════════════════"
} | tee -a "$LOG_FILE"

# ── 8. Run ────────────────────────────────────────────────────────────────────
CMD=(spark-pipelines run --spec spark-pipeline.yml)
CMD+=("$@")   # forward extra args, e.g. --full-refresh-all

echo "[INFO] Executing: spark-pipelines run --spec spark-pipeline.yml $*" | tee -a "$LOG_FILE"
"${CMD[@]}" 2>&1 | tee -a "$LOG_FILE"
EXIT_CODE=${PIPESTATUS[0]}

# ── 9. Cleanup runtime conf (contains secrets) ────────────────────────────────
rm -rf "$RUNTIME_CONF_DIR"

# ── 10. Result ────────────────────────────────────────────────────────────────
if [ "$EXIT_CODE" -eq 0 ]; then
    echo "[SUCCESS] Pipeline completed at $(date -Iseconds)" | tee -a "$LOG_FILE"
