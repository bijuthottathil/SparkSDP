#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────────────────
# LDP Tax Pipeline — Run Script
#
# Usage:
#   ./run.sh                     # normal cloud run
#   ./run.sh --full-refresh-all  # force full recompute
#   LOCAL_MODE=true ./run.sh     # offline test with sample CSV (no cloud)
#
# The script:
#   1. Loads credentials from .env
#   2. Derives storage/warehouse paths (ADLS or local fallback)
#   3. Writes a runtime spark-defaults.conf (credentials + warehouse dir)
#      and points SPARK_CONF_DIR at it so the Spark JVM reads it on startup.
#      This is required because Spark Connect server configs must be set
#      BEFORE the JVM starts — --conf flags only reach the client.
#   4. Generates spark-pipeline.yml from the template (env-var substitution)
#   5. Runs spark-pipelines
# ─────────────────────────────────────────────────────────────────────────────
set -euo pipefail

# Pin Java 17 explicitly — cron does not source shell profiles so JAVA_HOME
# would otherwise fall back to the system default (Java 25+), which blocks
# javax.security.auth.Subject.getSubject() used by Hadoop security.
export JAVA_HOME="/Library/Java/JavaVirtualMachines/temurin-17.jdk/Contents/Home"
export PATH="${JAVA_HOME}/bin:${PATH}"

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
export SCRIPT_DIR
cd "$SCRIPT_DIR"

# Capture any values the caller set on the command line BEFORE .env overwrites them
_CLI_LOCAL_MODE="${LOCAL_MODE:-}"

# ── 1. Load .env ──────────────────────────────────────────────────────────────
if [ -f "$SCRIPT_DIR/.env" ]; then
    set -a
    # shellcheck source=.env
    source "$SCRIPT_DIR/.env"
    set +a
else
    echo "[WARN] .env not found — using environment variables already set."
    echo "       Copy .env.example → .env and fill in your credentials."
fi

# Command-line value wins over .env (e.g. LOCAL_MODE=true ./run.sh)
if [ -n "$_CLI_LOCAL_MODE" ]; then
    LOCAL_MODE="$_CLI_LOCAL_MODE"
else
    LOCAL_MODE="${LOCAL_MODE:-false}"
fi

# ── 2. Derived paths ──────────────────────────────────────────────────────────
if [ "$LOCAL_MODE" = "true" ]; then
    LDP_STORAGE_PATH="file://${SCRIPT_DIR}/checkpoints"
    LDP_WAREHOUSE_PATH="file://${SCRIPT_DIR}/spark-warehouse"
    echo "[INFO] LOCAL_MODE=true — using local storage paths."
else
    : "${ADLS_ACCOUNT_NAME:?'ADLS_ACCOUNT_NAME must be set in .env'}"
    : "${ADLS_ACCOUNT_KEY:?'ADLS_ACCOUNT_KEY must be set in .env'}"
    : "${AWS_ACCESS_KEY_ID:?'AWS_ACCESS_KEY_ID must be set in .env'}"
    : "${AWS_SECRET_ACCESS_KEY:?'AWS_SECRET_ACCESS_KEY must be set in .env'}"

    _ADLS_ROOT="abfs://medallion@${ADLS_ACCOUNT_NAME}.dfs.core.windows.net/ldp-tax"
    LDP_STORAGE_PATH="${_ADLS_ROOT}/checkpoints"
    LDP_WAREHOUSE_PATH="${_ADLS_ROOT}/warehouse"
    echo "[INFO] CLOUD MODE — warehouse: ${LDP_WAREHOUSE_PATH}"

    JAR_DIR="${SCRIPT_DIR}/jars"
    if [ ! -d "$JAR_DIR" ] || [ -z "$(ls -A "$JAR_DIR" 2>/dev/null)" ]; then
        echo "[ERROR] Cloud mode requires JARs in ${JAR_DIR}/"
        echo "        Run ./download_jars.sh first."
        exit 1
    fi
    echo "[INFO] Loaded JARs: $(ls "$JAR_DIR"/*.jar | xargs -n1 basename | tr '\n' ' ')"
fi

export LDP_STORAGE_PATH LDP_WAREHOUSE_PATH

# ── 3. Write runtime spark-defaults.conf ──────────────────────────────────────
# Spark reads SPARK_CONF_DIR/spark-defaults.conf before the JVM starts.
# This is the ONLY reliable way to inject credentials into a Spark Connect
# server — --conf flags only reach the client side.
RUNTIME_CONF_DIR="${SCRIPT_DIR}/.spark-conf-runtime"
mkdir -p "$RUNTIME_CONF_DIR"

# Copy the original Spark conf so log4j etc. still work
SYSTEM_CONF_DIR="${SPARK_HOME:-/opt/homebrew/opt/apache-spark/libexec}/conf"
if [ -d "$SYSTEM_CONF_DIR" ]; then
    cp -n "$SYSTEM_CONF_DIR"/* "$RUNTIME_CONF_DIR"/ 2>/dev/null || true
fi

# Write the runtime defaults (overwrites any system defaults)
{
    echo "# ── Generated by run.sh at $(date -Iseconds) ──"
    echo "spark.sql.warehouse.dir        ${LDP_WAREHOUSE_PATH}"
    echo "spark.sql.shuffle.partitions   4"

    if [ "$LOCAL_MODE" != "true" ]; then
        echo "spark.hadoop.fs.s3a.access.key                                          ${AWS_ACCESS_KEY_ID}"
        echo "spark.hadoop.fs.s3a.secret.key                                          ${AWS_SECRET_ACCESS_KEY}"
        echo "spark.hadoop.fs.s3a.endpoint.region                                     ${AWS_REGION:-us-east-1}"
        echo "spark.hadoop.fs.s3a.impl                                                org.apache.hadoop.fs.s3a.S3AFileSystem"
        echo "spark.hadoop.fs.s3a.aws.credentials.provider                            org.apache.hadoop.fs.s3a.SimpleAWSCredentialsProvider"
        echo "spark.hadoop.fs.azure.account.auth.type.${ADLS_ACCOUNT_NAME}.dfs.core.windows.net  SharedKey"
        echo "spark.hadoop.fs.azure.account.key.${ADLS_ACCOUNT_NAME}.dfs.core.windows.net        ${ADLS_ACCOUNT_KEY}"
    fi
} > "${RUNTIME_CONF_DIR}/spark-defaults.conf"

export SPARK_CONF_DIR="$RUNTIME_CONF_DIR"
echo "[INFO] Runtime spark-defaults.conf written to ${RUNTIME_CONF_DIR}/"

# ── 4. Generate spark-pipeline.yml from template ──────────────────────────────
python3 - <<'PY'
